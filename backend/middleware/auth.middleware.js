import jwt from "jsonwebtoken";
import Tenant from "../model/tenant.model.js";
import Landlord from "../model/landlord.model.js";
import Admin from "../model/admin.model.js";

export const tenantProtect = async (req, res, next) => {
  console.log("In tenantProtect middleware");
  let token;
  if (req.headers.authorization && req.headers.authorization.startsWith("Bearer")) {
    try {
      token = req.headers.authorization.split(" ")[1];
      const decoded = jwt.verify(token, process.env.JWT_SECRET);

      req.user = await Tenant.findById(decoded.id).select("-password");
      return next();
    } catch (error) {
      return res.status(401).json({ message: "Not authorized, token failed" });
    }
  }

  if (!token) {
    return res.status(401).json({ message: "Not authorized, no token" });
  }
};

